// Switch - condicional

let opcao = 1;
switch (opcao) {
    case 1:
        // Se opção = 1;
        console.log("Opção 1 foi selecionada");
        break;

    case 2:
        // Se opção = 2;
        console.log("Opção 2 foi selecionada");
        break;

    default:
        // Se opção não for encontrada
        console.log("Opção inválida");
        break;
}