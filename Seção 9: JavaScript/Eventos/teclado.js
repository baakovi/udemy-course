/*
    Eventos - teclado

    - onkeydown: acionado no momento em que uma tecla é pressionada (soltando ou não a tecla)

    - onkeypress: acionado no momento em que uma tecla é pressionada (soltando-a ou não), porém não captura todas as teclas, como por exemplo - alt, crtl, esc, shift, etc.

    - onkeyup: acionada no momento em que a tecla é liberada.
*/